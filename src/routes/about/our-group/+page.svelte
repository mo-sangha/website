	<h2 class="page-header">Our Group</h2>
	<div class="page-content-section">
		<p>
			<a href="/" class="link link-hover text-secondary">MeditationOnline.org</a> has been an open
			meditation group since 2013, totally free of charge. We started off on Reddit's
			<a
				href="https://www.reddit.com/r/Buddhism/"
				target="_blank"
				class="link link-hover text-secondary"
				rel="noopener nofollow noreferrer">/r/Buddhism</a
			> subreddit as "redditations." Our online sessions are safe spaces where you can join us from anywhere
			in the world. There is no expectation of donations or payment of any kind. We are a community of
			people who are interested in meditation and mindfulness, and we're happy to have you join us.
		</p>
		<p>
			This group is intended to be all inclusive and modern in the sense of creating a new kind of
			space. Every person can have a voice and a kind of ownership within the group. Traditionally
			it's known that every sentient being is ultimately a Buddha so in that sense we can empower
			one another with minimum use of hierarchy while still preserving lineage and transmission. A
			grass roots, very human, and accessible approach presented in harmony with modern science and
			traditional methodology.
		</p>

		<p>
			The lineage and transmission is very important with practices like Dzogchen, Mahamudra and
			Zen. Our group is under the guidance of <a
				class="link-hover text-secondary"
				href="/about/lama-dawai-gocha">Lama Dawai Gocha</a
			>, who has gotten empowerments, transmission, and pointing out instructions from living
			recognized masters. Following your teacher's advice and doing any preliminary practices is
			recommended. If the practitioner is ready then they can begin Dzogchen which, in a modern
			sense, can start with an introduction to the nature of mind.
		</p>

		<p>Please come and go as you feel, there are no rules about attendance.</p>
